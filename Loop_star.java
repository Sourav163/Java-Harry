package Java_Harry;

public class Loop_star {
    public static void main(String[] args) {
        System.out.println("");
        for (int i = 4; i > 0; i--) {
            for (int j = i; j > 0; j--) {
                System.out.print("*");
            }
            System.out.println("");
        }
    }
}
